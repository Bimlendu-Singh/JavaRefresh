public class ArithematicOperations {
 
    public static void main(String[] args) {
        int a=9,b=3,c=4;
        float d=5;

        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println(a/d);
        System.out.println(a+d);
        System.out.println(a*d);
        System.out.println(a-d);
    }
}